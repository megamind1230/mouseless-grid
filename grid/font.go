// DrawChar draws a single character at (x, y) with given scale and color
func DrawChar(img *image.NRGBA, ch rune, x, y, scale int, c color.Color) {
	glyph, ok := tinyFont[ch]
	if !ok {
		return
	}
	for row := 0; row < 6; row++ {
		for col := 0; col < 4; col++ {
			if glyph[row]&(1<<uint(col)) != 0 {
				// draw scaled pixel
				for sy := 0; sy < scale; sy++ {
					for sx := 0; sx < scale; sx++ {
						px := x + col*scale + sx
						py := y + row*scale + sy
						if px >= 0 && px < img.Bounds().Dx() && py >= 0 && py < img.Bounds().Dy() {
							img.Set(px, py, c)
						}
					}
				}
			}
		}
	}
}

// DrawString draws a string centered at (cx, cy)
func DrawString(img *image.NRGBA, s string, cx, cy, scale int, c color.Color) {
	charW := 4 * scale
	charH := 6 * scale
	totalW := len(s) * charW
	startX := cx - totalW/2
	startY := cy - charH/2
	for i, ch := range s {
		DrawChar(img, ch, startX+i*charW, startY, scale, c)
	}
}
